function mm2pt(mm) {
    //1 pt = 1/72 inch = 25.4/72 mm = 0.35277777777738178 mm
    return mm / 0.35277777777738178;
}

function pt2mm(pt) {
    //1 pt = 1/72 inch = 25.4/72 mm = 0.35277777777738178 mm
    return pt * 0.35277777777738178;
}

function pt2cm(pt) {
    //1 pt = 1/72 inch = 25.4/72 mm = 0.35277777777738178 mm
    return pt * 0.035277777777738178 / 10;
}